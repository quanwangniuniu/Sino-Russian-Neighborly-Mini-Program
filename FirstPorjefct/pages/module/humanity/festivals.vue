	<uni-section title="九．儿童节—6月1日" type="line">
		<uni-group title="起源" mode="card">
			<view> 
			说起儿童节的来历，与俄罗斯有重要关联。1949年11月，国际民主妇女联合会在莫斯科举行理事会议，各国代表愤怒地揭露了帝国主义分子和各国反动派残杀、毒害儿童的罪行。为了保障世界各国儿童的生存权、保健权和受教育权，改善儿童的生活，会议决定以每年的6月1日为国际儿童节。
			 </view>
		</uni-group>
	
		<uni-group title="习俗" mode="card">
			<view>
			俄罗斯的儿童节和国际完全“合拍”，就在6月1日。每当儿童节来临的时候，俄罗斯各地的孩子们都会兴高采烈地欢度自己的节日，还会表演一些民族歌舞，学校里则举行庆祝活动。
			 </view>
		</uni-group>
		<image class="section-image" src="../../../static/images/festival9.jpg"></image>
		
	</uni-section>
	<uni-section title="十．诗歌节—6月6日" type="line">
		<uni-group title="起源" mode="card">
			<view> 
			俄罗斯诗歌节 6月6日是俄罗斯的诗歌节。这个节日是为了纪念俄罗斯伟大诗人普希金而设的，因为1799年的6月6日是诗人普希金的诞生日。
			 </view>
		</uni-group>
	
		<uni-group title="习俗" mode="card">
			<view> 
			每年这天，俄罗斯各地都举行形式多样的纪念活动。不同年龄、不同籍别的人们纷纷用各种语言朗诵普希金的作品，人们在这一天说的每一句话都与诗人和他的作品有关，以表达对诗人的挚爱。1880年莫斯科普希金塑像落成以来，每逢诗人的生日，莫斯科普希金广场便成为令人瞩目的中心，成了诗的海洋。大批的诗歌爱好者和普希金迷们便从四面八方聚集到普希金广场两侧的街心花园，他们手持鲜花和诗稿，从早到晚站在普希金的雕像前或大声吟诵诗人的不朽作品，或将自己的诗作拿出来相互交流。人们完全沉浸在诗歌的海洋之中。
			</view>
		</uni-group>
	</uni-section>
	<uni-section title="十一．独立日（俄罗斯日）—6月12日" type="line">
		<uni-group title="起源" mode="card">
			<view> 1990年这一天，俄罗斯联邦第一次人民代表大会以压倒性多数(接近百分之百)通过了俄罗斯苏维埃联邦社会主义共和国主权宣言。整整一年之后，举行了俄罗斯第一届总统的选举。
1994年，通过主权宣言日被确定为全国性的俄罗斯节日，1998年它获得了名称--俄罗斯日。俄罗斯日可以认为是新国家诞生的日子，是民主改革的起点。
           </view>
		</uni-group>
	
		<uni-group title="习俗" mode="card">
			<view>
				这个节日为俄罗斯人带来了连续4天的假期。已经成了传统：官方的活动尽量少，人民大联欢、广场演出、体育比赛、音乐与娱乐活动增多。当天，莫斯科城中到处张灯结彩，灯上挂着红旗。电视台不会举办国庆晚会，但各大频道会播出一些有关国庆的新闻，比如某城市举办国庆游行，总统拜访著名作家，为巡航导弹研发人员颁发特别奖等。俄罗斯日也是青年的节日，是在民主的俄罗斯国度内走向人生的青年一代的节日。
			 </view>
		</uni-group>
		<image class="section-image" src="../../../static/images/festival11.jpg"></image>
		
