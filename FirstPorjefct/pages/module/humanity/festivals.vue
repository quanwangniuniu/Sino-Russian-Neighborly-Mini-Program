	<uni-section title="十三．青年节—6月最后一个星期的星期日" type="line">
		<uni-group title="起源" mode="card">
			<view> 每年的6月27日是俄罗斯是青年节。根据俄罗斯联邦青年事务委员会和俄罗斯国家青年联合会的倡议，俄罗斯总统于1993年6月24日颁布命令，确定每年的6月27日为俄罗斯青年节。
　　1993年前，该节称作苏维埃青年节，始于1958年，但具体日期不固定，为每年6月的最后一个星期日。
           </view>
		</uni-group>
	
		<uni-group title="习俗" mode="card">
			<view> 
			节日这天，男女青年们兴高采烈地参加各种庆祝活动。有些地区的青年在这一天开展各项义务劳动，大搞清洁卫生或者植树造林。有些地区举办各种文艺演出会、联欢会。有的年轻人利用节日的喜庆气氛举行婚礼，使得节日更加热闹。
			 </view>
		</uni-group>
		<image class="section-image" src="../../../static/images/festival13.jpg"></image>
		
