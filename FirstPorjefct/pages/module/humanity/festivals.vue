	<uni-section title="十四．伊里亚节—8月2日" type="line">
		<uni-group title="起源" mode="card">
			<view> 
			伊利亚神是很受俄罗斯东正教教徒尊敬。传说他诞生在巴勒斯坦的法维格城，长大后，为拯救受苦受难的庶民，他与暴君进行了勇敢的斗争，成为古犹太雷神、电神及其他天空力量神的综合形象，传入俄罗斯后，与斯拉夫雷神、电神融合为一。人们习惯认为他是总管雨、雹、雷、电的神。这一天开始季节变换，中午前是夏天，中午后是秋天。民间称：“伊利亚节到，大雨跟着来。”在这之前禁摘苹果。7月末8月初正是粮食成熟季节，是农事活动中最重要的时期，也是农民最担心的冰雹、大雨、雷电等自然灾害多发时期，因此，人们对伊利亚神备加敬畏。
			 </view>
		</uni-group>
	
		<uni-group title="习俗" mode="card">
			<view> 
			节前，人们攒钱购物，准备过节时敬神。教堂里，人群熙攘，大家尽量地赞颂伊利亚神，为他献上羊腿、蜂蜜、啤酒等各种供品，祈求他保佑丰收，一直要热闹二三天。这一天不能堆干草垛，否则，据说不是雷劈人，就是电烧村。但姑娘们要穿节日盛装去摘苹果，小伙子们只能帮忙，不能动手采摘。
			</view>
		</uni-group>
	</uni-section>
	<uni-section title="十五．知识节—9月1日" type="line">
		<uni-group title="起源" mode="card">
			<view> 沙皇时期，一般来说，新学年开始于秋季，但罗斯没有固定的开学日期。有些地区在8月底就开始上学了，有些地区则在晚秋，甚至在12月初。尽管如此，由于大多数学校都是教会学校，教会的新年是在9月1日庆祝（现在仍然如此）。因此，大多数学校于9月1日开学。
情况一直持续到20世纪30年代，直到苏联政府为所有教育机构规定了一个确切的开学日期，也为不同的年龄段规定了若干个不同的学年结束日期。
1980年，知识日在国家层面上被确立下来，但不是作为一个节日，而是一个普通的上学日。在这一天，学生们会在课前庄严列队。
最终，1984年，它成为一个官方节日。费奥多尔·费奥多罗维奇·布留霍韦茨基是相关法令的制定者之一，他可以被视为这一节日的创始人。
           </view>
		</uni-group>
	
		<uni-group title="习俗" mode="card">
			<view> 各级各类学校是庆祝9月1日“知识节”的主体。在俄罗斯，所有的教学机构(包括幼儿园、中小学、大学)，对9月1日这个节日安排都十分重视。庆祝“知识节”、迎接新同学是新学年开学第一天的主要任务，各学校都要举办形式多样的庆祝活动。
一般都有迎新大会、校长讲话欢迎新同学、学生艺术团体演出、学校情况介绍展览等。有的大学还会在当天举办体育比赛，在新生入学典礼上组织新同学宣读誓词。
9月1日这一天，俄罗斯各地到处可以看到手捧鲜花的一年级小学新生，这是他们人生的一个重要的开始，他们要在开学的第一天向老师敬献鲜花，以表达自己的敬意。
           </view>
		</uni-group>
		<image class="section-image" src="../../../static/images/festival15.jpg"></image>
		
	</uni-section>
	<uni-section title="十六．教师节—10月的第一个星期天 " type="line">
		<uni-group title="起源" mode="card">
			<view> 世界教科文组织以10月5日为国际教师节。自1994年以来，都用于纪念世界范围内的教师组织。目的是为教师们争取支持，并确保未来孩子的需要能得到满足。
在俄罗斯，教师节在1965到1994年间被定为十月的第一个周日。1994年后，俄罗斯的教师节就改为与世界教师节同步，即10月5日。
            </view>
		</uni-group>
	
		<uni-group title="习俗" mode="card">
			<view> 在俄罗斯，很多学生给老师送鲜花，节日送鲜花非常流行，既显得格调高雅，又不俗套。
在俄罗斯，赠送鲜花的朵数只能是单数， 通常是送3朵、5朵或7朵。按俄罗斯的习俗，白色象征着纯洁、清白，大多数情况下还是送白花。
           </view>
		</uni-group>
