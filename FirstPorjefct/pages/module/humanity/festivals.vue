	<uni-section title="十二．卫国战争纪念日—6月22日" type="line">
		<uni-group title="起源" mode="card">
			<view> 
			1941年6月22日，德国法西斯撕毁《苏德互不侵犯条约》，悍然入侵苏联，卫国战争随之爆发。战争持续了1418天，约2700万名苏联军民牺牲。6月22日这天被俄罗斯政府定为“纪念和哀悼日。
			 </view>
		</uni-group>
	
		<uni-group title="习俗" mode="card">
			<view> 
			这一天会举行悼念仪式。仪式上，为悼念战争英雄，普京单膝下跪，将一束玫瑰花放置在永恒之火纪念碑前，并低下头默哀了一分钟。鲜花按照传统被献放在永恒之火纪念碑旁，以及“英雄城市之巷”和"军事荣耀之城"的每块墓碑上。
