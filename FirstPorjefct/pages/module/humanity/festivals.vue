	<uni-section title="十七．十月革命节—11月7日" type="line">
		<uni-group title="起源" mode="card">
			<view> 苏联时期，11月7日是传统的全国性节日。每年的这一天，苏联都要在莫斯科红场上举行盛大的阅兵式，纪念十月革命。
1941年11月7日，苏联举行了最具历史性的阅兵式。当时，苏联正处于卫国战争期间，纳粹德国军队已兵临莫斯科城下，形势非常严峻。为了鼓舞士气，苏联政府在红场上举行了盛大的阅兵式，纪念十月革命胜利24周年。斯大林站在克里姆林宫斯巴斯基钟楼下的列宁墓上检阅苏联红军，并发表了鼓舞人心的演说。许多部队从阅兵式现场直接开赴前线。
这次红场阅兵显示了正义之师必定战胜邪恶的魄力与信心，极大地鼓舞了广大苏联官兵的斗志和全世界人民反法西斯战争胜利的决心。莫斯科保卫战以胜利告终，它粉碎了德军自二战开始以来不可战胜的神话，也成为苏联卫国战争的一个转折点。
在1990年的十月革命节，威武雄壮的苏联武装力量再次通过红场——这是苏联举行的历史上最后一次阅兵式。
苏联解体后，俄罗斯官方不再庆祝十月革命节，但这一天仍是休息日。1996年，俄罗斯总统叶利钦将11月7日这一天定为“和谐和解日”。2005年6月21日，俄罗斯修订的联邦法律《俄罗斯军人荣誉日和纪念日》规定，从2005年起，11月7日被当作“军人荣誉日。
          </view>
		</uni-group>
	
		<uni-group title="习俗" mode="card">
			<view> 在11月7日的“十月革命日”会有阅兵式，虽然从2005年起，“十月革命日”不再是法定休息日了，但普通百姓还是会在家中大办节日宴席，许多群众依然自发地组织起来，举行包括游行在内的各种庆典活动。11月7日那天，莫斯科几乎所有人都会走出家门，在大街上活动。他们并不是在逛街，而是感受节日气氛，街上会不时走来一支拉着手风琴和小提琴的花车游行队伍，队伍中的人们穿着俄罗斯传统服装，载歌载舞，花车上写着“永远的十月革命”等标语。他们的游行也不是一本正经的，每当游行队伍走来时，所有群众都会参与进去，无论男女老少，认识的还是不认识的人，都在一起欢呼跳跃。
俄国人还会在国庆日和“十月革命日”那天为烈士送上鲜花。那天，可以在红场烈士纪念碑周围看到很多花圈，但更多的还是群众自发献上的一支支鲜花，它们最后堆成一片花的海洋。莫斯科很多地方还有英烈的铜牌或战争遗址，国庆日和“十月革命日”时，也会堆满鲜花。
           </view>
		</uni-group>
