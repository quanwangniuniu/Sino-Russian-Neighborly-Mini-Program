	<uni-section title="五．谢肉节—复活节前的第8周" type="line">
		<uni-group title="起源" mode="card">
			<view> "谢肉节"源于东正教。在东正教为期40天的大斋期里，人们禁止吃肉和娱乐。因而，在斋期开始前一周，人们纵情欢乐，家家户户抓紧吃荤，以此弥补斋戒期苦行僧式的生活。"谢肉节"因此而得名。
"谢肉节"又叫送冬节。中世纪的斯拉夫民族认为，当太阳神雅利拉战胜了严寒和黑夜的时候，就是春天来临的日子。对农民来说，这意味着春耕劳动即将开始。因此，每年2月底、3月初，古斯拉夫人都要举行隆重的迎春送冬仪式，并由此产生了送冬节。
           </view>
		</uni-group>
	
		<uni-group title="习俗" mode="card">
			<view> 谢肉节最古老也是最重要的传统就是烹饪并促漏采壳食用名为blini的烤薄饼。这种薄饼呈金黄色，象征着太阳，表示春天的来临和白天越来越长。这种薄饼原先用荞麦粉、黄油、鸡蛋、牛奶、土豆或奶油、调味料制作而成，随着人们生活水品的提高，开始逐渐向其中加入面粉成分。食用的时候，用薄饼将鱼子酱或是三文鱼卷起。舞蹈，歌唱，滑雪，滑冰，拳击等体育活动也是谢肉节必不可少的节目。此外，谢肉节的一大特色是在这一周之中，每一天都有不同的庆祝方式。
谢肉节另一种娱乐方式是拳击，它一直保留至今。最不伤和气的玩乐则是从结冰的山坡上飞速滑下。很长时间以来，斯拉夫人一直将谢肉节视为新年。因为14世纪以前，俄罗斯的新年始于3月。而根据俄罗斯的风俗，新年那天运气如何，此后一年就会延续那天的运气。因此，每逢此节，俄罗斯人都不吝惜美酒佳肴，并纵情享乐。所以谢肉节在民间一直被称为"坦诚的"、"全民"的节日。
按照传统的民间习俗，为期7天的"谢肉节"每天都有不同的庆祝方式:星期一是迎春日 ，人们用稻草和布条捆扎象征冬天的玩偶，并将它们放在家里。入夜时分，人们燃起篝火，在篝火旁载歌载舞。第二天是娱乐日。周三是美食日，是女婿们到岳母家吃面饼的日子。接下来的一天是醉酒日，节日在这一天会达到高潮。星期五是女婿回请岳母吃面饼的日子。小姑子聚会日是在第六天，人们从这天开始焚烧用干草制成的玩偶，表示送走冬天，迎接春天的来到。最后一天是宽恕日，人们在这一天互相请求对方宽恕自己，摒弃前嫌，重归于好。这天，人们将那些还没有来得及焚烧的玩偶付之一炬，表示已彻底告别寒冷的冬天，迎来了阳光明媚的春天。
随着时代变迁，俄罗斯人已不再遵守那些陈旧的清规戒律，但烤制面饼、捆扎并焚烧玩偶的习俗仍然保留了下来。每当"谢肉节"来临，俄罗斯人都会自发组织化装游行、民间歌舞、游戏、溜冰、滑雪、乘三套马车兜风等娱乐活动。为推动莫斯科旅游业的发展，莫斯科市政府于2002年已正式宣布"谢肉节"为城市节日，并组织安排多项大型活动。俄罗斯许多地方的冬季长达半年左右。冬天的俄罗斯寒风凛冽、万物萧条，到处都是冰天雪地。这在很大程度上束缚了俄罗斯人活泼好动的天性。而"谢肉节"的来临则使俄罗斯人看到了春天的希望。
