			<view> 俄罗斯胜利节即“胜利日”，是前苏联为纪念战胜德国法西斯而设立的纪念性节日。
每年的5月9日是原苏联时期卫国战争胜利，德国投降书生效的日子。俄罗斯独立之后保留了这个节日，并改称为胜利节。
1995年4月19日，俄罗斯联邦国家杜马（议会下院）通过了永久纪念胜利日的法令，规定5月9日为全民性节日，全国放假一天。
           </view>
		</uni-group>
	
		<uni-group title="习俗" mode="card">
			<view> 
			每逢胜利日来临，人们都会以各种方式纪念那个历史性时刻。许多人在胸前和手臂佩戴一条黄黑条纹相间、被视为勇敢和胜利象征的“乔治丝带”，还有人把丝带系在汽车天线和反光镜上，有的年轻母亲把丝带系在孩子的小推车上，构成胜利日纪念活动中的一道亮丽风景。其中最虔诚的是二战老兵，他们是这个节日的主角，也是最受尊敬的人。
			 </view>
		</uni-group>
