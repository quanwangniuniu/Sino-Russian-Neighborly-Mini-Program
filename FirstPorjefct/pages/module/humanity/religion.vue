		    <view class="section-description">
		     <p>
				 随着历史的发展、生产力的提高、社会意识形态与制度的变更，多神教已逐渐不适应罗斯大公的统治需要。因此于公元988年，弗拉基米尔大公将基督教引进俄罗斯，令全基辅的人接受基督教的洗礼，成为上帝的子民，并将基督教定为国教，史称“罗斯受洗”。但是，罗斯的受洗并不等于罗斯一下子就基督教化。由于多神教的影响，使得罗斯的基督教化成为一个漫长而渐进的过程，在很长一段时间内，罗斯的宗教信仰都是多神教和基督教共存，因此在我国史学界有 “双重信仰”的说法，即在罗斯受洗后几百年内罗斯人既接受基督教的信仰，又不放弃对多神教诸神的崇拜。“在城市里，多神教的庙宇量然已被拆除，但市民家中仍习惯地敬奉着多神教的偶像；在基督教波及的乡村，人们在教堂里作完礼拜后，马上赶回仓房、树林或河边去祷告，生怕得罪了多神教古老的保护神”。即便是在罗斯彻底基督教化后，多神教的某些习俗依然保存了下来，俄罗斯人甚至将多神教的祭祀仪式和风俗习惯渗透到基督教中，与之融为一体，使基督教成为独具俄罗斯特色的东正教。
