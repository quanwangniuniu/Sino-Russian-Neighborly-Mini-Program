		    <view class="section-description">
		     <p>
1.俄罗斯宗教与建筑
宗教在人们之间起着一种红带作用，把他们联系在一起，形成一种势力，同时宗教又与社会生活的许多方面有看密切联系，互相影响、呈现出复杂的状况。宗教的复杂性，基辅罗斯石建筑中最具代表的是基辅的圣索非亚教堂。
12世纪中叶，古罗斯进人了封建割据时期。各地王公为了取得教会的支持，不惜耗费巨资，大兴土木，建造大教堂。这些教堂建筑基本保留了 11世纪的建筑风格和原则，但其结构趋向了简单化，同时具有了更加美观漂亮的外观造型和更加实用的结构。
12世纪后半叶，古罗斯出现了塔式教堂，这是拜占庭祭祀建筑对古罗斯建筑影响渐渐减弱的表现。其中斯摩棱斯克的天使长米加勒大教堂就是这种教堂的早期范例。古罗斯各个公国的教堂在继承拜占庭和基辅建筑艺术的同吋，又有各自的地区特色。这个时期宗教建筑的一个明显现象是形成了俄罗斯祭祀建筑的圆筒型塔式教堂建筑模式。
13世纪下半叶，因为蒙古鞅靶人的人侵和罗斯周边封建主的扩张，大量建筑精品被毁掉，罗斯的建筑业进人衰落和停湍状态。但是在没有破坏的城市诺夫哥罗德等地仍然进行着宗教建筑的建设。由于教堂很多是由大贵族，大商人出资兴建的，因此建筑在继承古罗斯传统时，有了一些新的表现方法来适应定制人的不同要求。科普纳的尼占拉教堂正是这种探索的表现。教堂正面按弃了原先的棱柱间隔方式，而是一个整体，墙皮也由原来单纯石灰墙皮变成石灰加石子墙皮，半侧祭坛由三个减到一个。教堂坚实有力。体现出诺夫哥罗德建筑艺术的风格。
14世纪下半叶到15世纪，库利科天会战反败为胜后，莫斯科在全罗斯的主导地被各地所承认。这时的东正教会鼎立扶持莫斯科大公，大公因此对东正教会非常依赖和提携，在各地建立了很多东正教堂。石头建筑在莫斯科崭露头角，莫斯科的石砌大教堂受弗拉基米尔的建筑影响较大。建筑物墙壁主要用白石砌成，规模并不是很大。
15世纪末到16世纪，教堂建设开始与西方文艺复兴时代的建筑艺术接触。以教建筑为重点和中心的莫斯科新克里姆林宫的建设，标志着以莫斯科为首都的俄罗斯中央集权国家的确立。角锥式教堂的出现是16世纪俄罗斯建筑的最高成就，它最大限度的体现出基于木头建筑学的俄罗斯传统的民族特色。
据历史学家的考证和建筑史界公认的标准来看，俄罗斯的教堂建筑风格被称作斯拉夫式教堂艺术风格，以与拜占庭式教堂艺术风格柏区分。它是拜占庭式教堂艺术与罗马教堂艺术、古罗斯神庙建筑艺术相结合的产物。而俄罗斯当地城市建筑受俄罗斯宗教教建筑影响颇深。
2.俄罗斯宗教及与之相关的习俗
（1）多神教与俄罗斯习俗
多神教产生于原始社会时期，源于认识能力的低下，因而崇拜对彖主要为自然现象和自然物，对于无法掌控和解释的事物或现象往往产生恐惧，常将其视为神秘力量。受多神教的影响俄罗斯形成了很多民间习俗。例如在俄罗斯人的牛活中家神具有至关重要的意义。而白猫则被俄罗斯人认为是家神的象征，因此俄罗斯几乎家家都会养一只片猫。每当俄罗斯人乔迁新居时都会先让自家白猫进去转一圈，主人才会人住，而搬离日屋时，白猫作为家神的代表则是最后一个离开的。类似的民间习俗还形成了俄罗斯熊节、俄罗斯鹅节、俄罗斯桦树节等民间节
（2）基督教与俄罗斯习俗
基督教传人俄罗斯后，经过传播和演变，逐渐具有俄罗斯本士特色。俄罗斯东正教一个比较具有代表性的特点就是圣像崇拜，每一个普通的东正教徒家中必定供奉着一幅圣像。当俄罗斯人接受了基督教之后，他们在困难或紧急关头总会一边划十字一遍向上帝铸告，除了供奉圣像、划十字、向上帝祷告等日常行为外，俄罗斯东正教在人出生和死亡后都分别设有重要的仪式，出生后需要接受洗礼，临死前要做临终涂油礼和临终忏悔，下葬后第9天、第40天、一周年，死者亲属要举行追荐亡灵的仪式。
（3）多神教与基督教混合体与俄罗斯习俗
这种混合首先体现为观念上，并通过俄罗斯人的言行举止表现出来。例如，在《大师与玛格兩特》这本书中有这样一个场景的描写 “在精神病院，当医生问伊万抓杀人凶手，为什么要拿圣像时，伊万的回答是“圣像最让他们这种人害怕了，因为他们这种人是跟鬼怪打交道的人，而鬼怪是書怕圣像的。”圣像崇拜是比较具有俄罗斯特色的宗教现象，按照赫克的观点，这皆缘于俄罗斯人对宗教象征主义的热爱。
圣像、圣物在俄罗斯人的宗教牛活中起着重要作用。圣像对老百姓的想象具有强烈的魅力，能激起他们朝圣这此神完的强烈愿望。不但如此，虔信的俄罗斯人在自家最尊贵的位置也会放置圣像，认为这可以驱魔群邪。而鬼怪的观念却很具有俄罗斯本土色彩，正如上文所提到的，是俄罗斯多神教的元素。无论是圣像崇拜，还是多神教的鬼怪观，经过儿百乃至上千年的传播，已经成为一种思想模因根植在俄罗斯人的集体无意识中，下意识地影响着他们的言行举止。		  	 </p>
