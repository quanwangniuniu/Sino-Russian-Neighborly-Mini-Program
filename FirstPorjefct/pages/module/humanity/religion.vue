             <p>
				 人是自然界最复杂的生物，特别在思想和精神方面比白然界其它生物都要复杂，而且人的行动是受思想支配的。而俄罗斯的宗教起源则来源于对自然力的敬畏，对自然界种种现象的思考等。
				 俄罗斯在基辅罗斯时代是一个信仰多神教的国家，古罗斯人的多神教崇拜和祭祀的对象首先是江湖河泊、树木丛林，这是与古东斯拉夫人以待猎、捕鱼、采集为主的生产方式分不开的。后来，古东斯拉夫人的生产方式发生了变化，狩猎、捕鱼、采集逐渐被农业、手工业所代替，相应地，他们崇拜和祭祀的对象也有所变化，但对水神的崇拜却一直保留了下机。有人将古东斯拉夫人的多神教信仰对象分为三类：第一类是“吸血鬼”和“保护神”，吸血鬼为另性，代表着恶的开端，保护神则是一些女性，太阳、水域、花朵及其他植物的图案就是保护神的象征；第二类是多产神和家宅女神；第三类是众神Z首别龙，别龙是雷神、战神，他统领着太阳神达日博格及财产与家畜的保护神霍尔斯、风神斯特里博格、水神谢马尔格尔、手工业及商业保护神莫科什。此外，古罗斯人还敬奉火神、天神等。古罗斯多神教是罗斯人的宗教信仰，人们对诸神的崇拜祭祀活动和仪式己成为日常牛活和牛产活动的重要内容。古罗斯多神教自产生之日起，一直影响和作用着罗斯社会的发展进程，而且这种社会意识随着古罗斯人的意识和劳动活动的发展逐步发展起来，到10世纪中叶己达到相当普及的程度，成为古罗斯人和古罗斯社会的主要意识形态。
