			<view> 俄罗斯新年是一年中最为隆重，最具民族特色的俄罗斯传统节日。每一年的1月初，是俄罗斯人狂欢的日子，新年、圣诞汇聚于此，长达3个星期的狂欢日，除了在餐桌上度过，长久以来，俄罗斯人习惯于购买新年礼物，添置欢乐饰物，举办家庭派对，或者出国旅游。和其他西方国家一样，每逢新年，每家每户，大街小巷，都会布置得很漂亮。但与其他国家不一样的是，俄罗斯人往往坚持用真的枞树和彩灯，而非人造枞树来装点中央广场，以及家里的客厅。甚至在莫斯科，所有的商店和餐厅，都必须在新年前装扮一新，否则要被罚款。节日当天家家张灯结彩，欢声笑语，大家聚在新年枞树边迎接新的一年的到来。直到1月19日的耶稣受洗节，人们仍然保持着高涨的情绪，继续制作美味的菜肴，继续到亲朋好友家串门。
俄罗斯新年的餐桌是格外丰盛的，各式的开胃冷盘，热菜，甜点，让人垂涎欲滴。应当说明的是，每家新年的餐桌上香槟是必不可少的，克里姆林宫自鸣钟响过12点时，播音员就向全国人民祝贺新年。此时，所有的人都停止了一切活动，高呼“乌拉”，相互祝贺，开怀畅饮。按俄罗斯习俗，庆祝新年应该先喝香槟酒，然后才可以喝其它酒享用新年晚餐。
和西方的圣诞老人类似，俄罗斯民族传统中也有一位和蔼的老爷爷形象，他就是严冬老人。在俄罗斯人的眼里，“严冬老人”从天空中飘下的第一片雪花开始就存在了，并非只是在节日才出现。严冬老人每次在新年期间的亮相时，身边总会跟着自己的孙女雪姑娘，她头戴俄罗斯传统珠冠，为孩子们送上祝福。在俄罗斯人民心目中，新年的庆祝活动绝不能少了雪爷爷和雪姑娘。
