			<view> 最先，俄罗斯民族的新年是每年初春时分的3月1日。因为俄罗斯大地靠近北极地区，春季到来的时间要比其它地区来的晚。那时候，每年的3月1日当天都会举行祭祀太阳的隆重仪式。15世界末期，受到拜占庭的影响，俄罗斯又将新年改至每年的9月1日，并一直持续到1699年才由彼得大帝依照西欧各国的传统习惯改为今天的1月1日。1699年除夕午夜，彼得大帝在莫斯科红场上点燃了第一枚礼花，并命令莫斯科居民用松、柏、枞等常青树装房屋，以示吉祥，塔形的枞树象征着生命常青，此后枞树成为圣诞节和新年的主要标志。
